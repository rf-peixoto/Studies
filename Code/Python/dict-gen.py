# ============================================================================================================================
# ID&Keys Generator
# Python 3.8
# ============================================================================================================================
import secrets

# ============================================================================================================================
# CORE CONFIG
# ============================================================================================================================
active_id = 0
ids = []
keys = []
result = {}

# ============================================================================================================================
# USER CONFIG
# ============================================================================================================================
number_of_keys = int(input("Number of keys: "))
bytes = int(input("Keys with ? bytes: "))

# ============================================================================================================================
# GENERATE
# ============================================================================================================================
while active_id <= number_of_keys:
    ids.append(active_id + 1)
    keys.append(secrets.token_hex(bytes))
    active_id += 1

# ============================================================================================================================
# CONCAT & SAVE
# ============================================================================================================================
result = dict(zip(ids, keys))
with open("result.txt", "w") as file:
    for item in result:
        file.write("{0}:{1}\n".format(item, result[item]))
    file.close()

input("Press anything to quit.")